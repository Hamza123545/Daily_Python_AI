# Integer
integer_value = 10
print("Integer:", integer_value, type(integer_value))

# Float
float_value = 10.5
print("Float:", float_value, type(float_value))

# String
string_value = "Hello, Python!"
print("String:", string_value, type(string_value))

# Boolean
boolean_value = True
print("Boolean:", boolean_value, type(boolean_value))

# List
list_value = [1, 2, 3, "Python", 4.5]
print("List:", list_value, type(list_value))

# Tuple
tuple_value = (1, 2, 3, "Tuple", 5.5)
print("Tuple:", tuple_value, type(tuple_value))

# Set
set_value = {1, 2, 3, 4, "Set"}
print("Set:", set_value, type(set_value))

# Dictionary
dict_value = {"name": "Ameen", "age": 21, "city": "Karachi"}
print("Dictionary:", dict_value, type(dict_value))

# None Type
none_value = None
print("None Type:", none_value, type(none_value))

# Bytes
bytes_value = b"Hello"
print("Bytes:", bytes_value, type(bytes_value))

# Bytearray
bytearray_value = bytearray([65, 66, 67])
print("Bytearray:", bytearray_value, type(bytearray_value))

# Range
range_value = range(5)
print("Range:", range_value, type(range_value))

# Frozen Set
frozen_set_value = frozenset([1, 2, 3, 4])
print("Frozen Set:", frozen_set_value, type(frozen_set_value))